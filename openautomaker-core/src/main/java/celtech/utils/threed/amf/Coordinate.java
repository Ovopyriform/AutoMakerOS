package celtech.utils.threed.amf;

/**
 *
 * @author Ian
 */
public class Coordinate {

	private float x = 1.00f;
	private float y = 0.79f;
	private float z = 0.19f;

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public float getZ() {
		return z;
	}

	public void setZ(float z) {
		this.z = z;
	}

}
