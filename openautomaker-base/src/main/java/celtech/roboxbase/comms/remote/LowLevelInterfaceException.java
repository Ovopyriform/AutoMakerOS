
package celtech.roboxbase.comms.remote;

/**
 *
 * @author ianhudson
 */
public class LowLevelInterfaceException extends Exception
{

    /**
     * Creates a new instance of <code>LowLevelInterfaceException</code> without
     * detail message.
     */
    public LowLevelInterfaceException()
    {
    }

    /**
     * Constructs an instance of <code>LowLevelInterfaceException</code> with
     * the specified detail message.
     *
     * @param msg the detail message.
     */
    public LowLevelInterfaceException(String msg)
    {
        super(msg);
    }
}
